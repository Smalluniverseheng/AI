  function apply() {
    const l = current();
    document.documentElement.lang = l.id;
    document.documentElement.dir = l.dir;
    /* 通用属性方式 */
    document.querySelectorAll('[data-i18n-text]').forEach(el => { el.textContent = t(el.dataset.i18nText); });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => { el.placeholder = t(el.dataset.i18nPh); });
    document.querySelectorAll('[data-i18n-title]').forEach(el => { el.title = t(el.dataset.i18nTitle); });
    /* 选择器映射方式（保留图标结构） */
    STATIC.forEach(([sel, key, attr]) => {
      const el = document.querySelector(sel);
      if (!el) return;
      const val = t(key);
      if (attr === 'ph') el.placeholder = val;
      else if (attr === 'title') el.title = val;
      else {
        // 保留按钮内图标：仅替换最后一个文本节点
        const nodes = [...el.childNodes].filter(n => n.nodeType === 3);
        if (nodes.length) nodes[nodes.length - 1].textContent = val;
        else el.appendChild(document.createTextNode(val));
      }
    });
    updateInputPlaceholder();
    document.dispatchEvent(new CustomEvent('langchange'));
  }

  function updateInputPlaceholder() {
    const el = document.getElementById('chatInput');
    if (!el) return;
    const short = window.DeviceInfo && (DeviceInfo.isMobile || DeviceInfo.isWatch);
    el.placeholder = short ? t('input.phShort') : t('input.ph');
  }

  function setLang(id) {
    if (!LANGS.some(l => l.id === id)) return;
    Store.state.lang = id;
    Store.save();
    apply();
  }

  return { LANGS, lang, t, current, speechLang, mimoAsrLang, langHintForModel, setLang, apply, updateInputPlaceholder };
