  /* ---------- 核心 ---------- */
  function lang() { return Store.state.lang || 'zh-CN'; }

  function t(key) {
    const l = lang();
    return (DICT[l] && DICT[l][key]) || DICT['zh-CN'][key] || key;
  }

  function current() { return LANGS.find(l => l.id === lang()) || LANGS[0]; }

  /* 语音相关语言代码（TTS / 浏览器语音识别使用） */
  function speechLang() { return current().bcp47; }

  /* 小米 ASR 语言参数（仅支持 zh / en / auto） */
  function mimoAsrLang() { return lang().startsWith('zh') ? 'zh' : 'en'; }

